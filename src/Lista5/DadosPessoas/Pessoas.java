package Lista5.DadosPessoas;

public class Pessoas {
    private char genero;
    private double altura;

    public Pessoas(char genero, double altura) {
        this.genero = genero;
        this.altura = altura;
    }

    public char getGenero() {
        return genero;
    }

    public double getAltura() {
        return altura;
    }

}
